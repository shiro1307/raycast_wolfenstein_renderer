const SIZE = 800;
const WALL_DENSITY = 0.38; // tweak 0.3 → 0.45

function generateMap(size) {
    const map = [];

    for (let y = 0; y < size; y++) {
        const row = [];

        for (let x = 0; x < size; x++) {
            // borders always walls
            if (x === 0 || y === 0 || x === size - 1 || y === size - 1) {
                row.push(1);
            } else {
                row.push(Math.random() < WALL_DENSITY ? 1 : 0);
            }
        }

        map.push(row);
    }

    // ---- carve spawn area (guaranteed empty) ----
    const spawnX = 2;
    const spawnY = 2;

    for (let y = spawnY - 2; y <= spawnY + 2; y++) {
        for (let x = spawnX - 2; x <= spawnX + 2; x++) {
            map[y][x] = 0;
        }
    }

    // ---- carve some random corridors (playability + ray stress) ----
    for (let i = 0; i < 200; i++) {
        let x = Math.floor(Math.random() * size);
        let y = Math.floor(Math.random() * size);

        for (let j = 0; j < 10; j++) {
            if (x > 1 && x < size - 1 && y > 1 && y < size - 1) {
                map[y][x] = 0;
            }

            // random walk
            x += Math.floor(Math.random() * 3) - 1;
            y += Math.floor(Math.random() * 3) - 1;
        }
    }

    return map;
}

